public class Variaveis {
    public static void main(String[] args) {
        int inteiro = 2; // int pra números inteiros 
        double decimal = 5.5; // double para decimais
        String texto = "Olá, Mundo!"; // String precisa começar com maiúscula, String é uma classe
        char caractere = 'A'; // char é um único caractere, precisa de aspas simples ''
        boolean verdadeiro = true; // true e false são com letra minúscula

        System.out.println("Inteiro: " + inteiro); 
        System.out.println("Número com vírgula: " + decimal); 
        System.out.println("Texto: " + texto); 
        System.out.println("Letra única: " + caractere); 
        System.out.println("Verdadeiro: " + verdadeiro);
    }
}